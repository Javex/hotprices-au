exports.fromCategoryCode = (code) => {
   for (let i = 0; i < exports.categories.length; i++) {
      const category = exports.categories[i];
      for (let j = 0; j < category.subcategories.length; j++) {
         const subcategory = category.subcategories[j];
         if (subcategory.code === code) {
            return [i, j];
         }
      }
   }
   return [exports.categories.length - 1, 0]; // Return the last category by default if no match is found
};

